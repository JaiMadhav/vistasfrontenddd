// Helper functions
export const getPackageById = (id) => {
  return packages.find(pkg => pkg.id === parseInt(id));
};

export const getPackagesByState = (state) => {
  return packages.filter(pkg => pkg.state === state);
};

export const getPackagesByVillage = (village) => {
  return packages.filter(pkg => pkg.village === village);
};

export const getPackagesByDuration = (duration) => {
  return packages.filter(pkg => pkg.duration === duration);
};

export const getPackagesByType = (type) => {
  return packages.filter(pkg => pkg.type === type);
};

export const getPackagesByPriceRange = (minPrice, maxPrice) => {
  return packages.filter(pkg => pkg.price >= minPrice && pkg.price <= maxPrice);
};
