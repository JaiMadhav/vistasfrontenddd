export const getHandicraftById = (id) => {
  return handicrafts.find(handicraft => handicraft.id === parseInt(id));
};

export const getHandicraftsByState = (state) => {
  return handicrafts.filter(handicraft => handicraft.state === state);
};

export const getHandicraftsByVillage = (village) => {
  return handicrafts.filter(handicraft => handicraft.village === village);
};

export const getHandicraftsByCategory = (category) => {
  return handicrafts.filter(handicraft => handicraft.category === category);
};
